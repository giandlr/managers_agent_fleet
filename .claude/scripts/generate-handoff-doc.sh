#!/usr/bin/env bash
set -euo pipefail

# ─────────────────────────────────────────────────────────────
# Generate IT Handoff Document
#
# Auto-generates docs/deployment-guide.md from project state.
# Reads CLAUDE.md, requirements.txt, .env.local to produce a
# complete setup guide for IT teams to deploy to Vercel + Supabase Cloud.
#
# Usage: bash .claude/scripts/generate-handoff-doc.sh
# ─────────────────────────────────────────────────────────────

echo ""
echo "═══════════════════════════════════════════════════════════"
echo " Generating IT Handoff Document"
echo "═══════════════════════════════════════════════════════════"
echo ""

# Extract app name from CLAUDE.md
APP_NAME="[APP_NAME]"
if [ -f "CLAUDE.md" ]; then
    EXTRACTED=$(grep -m1 "^## Project:" CLAUDE.md 2>/dev/null | sed 's/## Project: //' || true)
    if [[ -n "$EXTRACTED" && "$EXTRACTED" != "[APP_NAME]" ]]; then
        APP_NAME="$EXTRACTED"
    fi
fi

# Extract Python deps
PYTHON_DEPS=""
if [ -f "backend/requirements.txt" ]; then
    PYTHON_DEPS=$(cat backend/requirements.txt)
fi

# Extract env var names from .env.local
ENV_VARS=""
if [ -f ".env.local" ]; then
    ENV_VARS=$(grep -E "^[A-Z_]+" .env.local | cut -d= -f1 || true)
fi

# Extract frontend env vars
FRONTEND_ENV_VARS=""
if [ -f "frontend/.env" ]; then
    FRONTEND_ENV_VARS=$(grep -E "^[A-Z_]+" frontend/.env | cut -d= -f1 || true)
fi

mkdir -p docs

cat > docs/deployment-guide.md << DOCEOF
# Deployment Guide — $APP_NAME

> Auto-generated by Sprout toolkit on $(date '+%Y-%m-%d %H:%M:%S').
> This guide helps your IT team deploy the application to Vercel + Supabase Cloud.

