## 1. Vercel Setup

### Create Team & Import Repository

1. Go to [vercel.com](https://vercel.com) → Create a new Team
2. Enable SSO/SAML if using enterprise identity provider
3. Import this Git repository
4. Vercel will auto-detect the Nuxt 3 framework
5. Set the **Root Directory** to \`.\` (project root)
6. Set the **Build Command** to \`cd frontend && npm run build\`
7. Set the **Output Directory** to \`frontend/.output\`

### Enable Deployment Protection

1. Go to Project Settings → Deployment Protection
2. Enable **Vercel Authentication** for preview deployments
3. Only production deploys should be publicly accessible

### Set Environment Variables

In Vercel Project Settings → Environment Variables, add:

| Variable | Environment | Value |
|----------|-------------|-------|
| \`SUPABASE_URL\` | All | Your Supabase project URL |
| \`SUPABASE_ANON_KEY\` | All | Supabase anon/public key |
| \`SUPABASE_SERVICE_ROLE_KEY\` | All | Supabase service role key |
| \`NUXT_PUBLIC_SUPABASE_URL\` | All | Same as SUPABASE_URL |
| \`NUXT_PUBLIC_SUPABASE_ANON_KEY\` | All | Same as SUPABASE_ANON_KEY |
| \`NUXT_PUBLIC_API_BASE_URL\` | Production | \`https://your-domain.vercel.app/api\` |
| \`PRODUCTION_URL\` | Production | \`https://your-domain.vercel.app\` |
| \`APP_NAME\` | All | $APP_NAME |

> **Security:** \`SUPABASE_SERVICE_ROLE_KEY\` is used by the backend API only. It bypasses Row Level Security and must never be exposed to the client.

---

## 2. Supabase Cloud Setup

### Create Project

1. Go to [supabase.com](https://supabase.com) → Create a new project
2. Choose **Pro** plan (recommended) or **Enterprise** for SOC2/HIPAA compliance
3. Select the region closest to your users
4. Save the project URL and API keys

### Apply Migrations

From the developer's local machine:

\`\`\`bash
# Link to your cloud project
supabase link --project-ref YOUR_PROJECT_REF

# Push all migrations
supabase db push

# Verify
supabase db lint
\`\`\`

### Configure Database Branching (Optional)

For staging/preview environments:

1. Go to Supabase Dashboard → Branching (Pro plan required)
2. Enable branching for preview deploy testing
3. Each PR gets its own database branch

