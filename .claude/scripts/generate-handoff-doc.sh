## 4. Security Checklist

- [ ] Deployment protection enabled on Vercel (preview deploys require auth)
- [ ] All environment variables set in Vercel dashboard (no secrets in code)
- [ ] \`SUPABASE_SERVICE_ROLE_KEY\` only in backend environment, not frontend
- [ ] Row Level Security (RLS) enabled on all Supabase tables
- [ ] CORS configured to allow only the production domain
- [ ] HTTPS enforced (Vercel provides this automatically)
- [ ] Security headers configured in \`vercel.json\` (X-Content-Type-Options, X-Frame-Options, HSTS)
- [ ] Supabase database password is strong and unique
- [ ] Supabase Auth email templates customized (no default branding)
- [ ] Rate limiting configured on auth endpoints

---

## 5. Monitoring & Rollback

### Monitoring

- **Vercel Dashboard:** Deployment logs, function invocations, error rates
- **Supabase Studio:** Database queries, auth events, storage usage, realtime connections
- **Logs:** Vercel → Logs tab shows serverless function output (structured JSON)

### Rollback

If a deployment causes issues:

\`\`\`bash
# Option 1: Revert in Vercel dashboard
# Go to Deployments → select previous deployment → Promote to Production

# Option 2: Git revert
git revert HEAD
git push origin main
# Vercel auto-deploys the revert
\`\`\`

### Database Rollback

> **Important:** Database migrations cannot be automatically reverted. Always test migrations on a branch database first.

---

## 6. Branch Strategy

\`\`\`
feature-branch ──▶ PR (preview deploy + branch DB) ──▶ main (production deploy)
\`\`\`

- **Preview deploys:** Every PR gets a unique URL for testing
- **Production deploys:** Only on merge to \`main\`
- **Database branching:** Enable in Supabase for isolated testing per PR

